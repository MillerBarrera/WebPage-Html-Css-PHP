    <p>
      Soy una persona que a dedicado 25 años de trabajo con niños y niñas desde la primera infancia. Comencé con Hogares Comunitarios de ICBF como Madre Comunitaria donde mi labor era cuidar los niños y niñas mientras que su madre salía a trabajar para ayudar al sustento de sus familias. Esta labor la desempeñe durante 10 años.
Luego pase a modalidad familiar como agente educativo, es decir que nuestra atención se basa en atención a familias desde la gestación hasta los dos años. Las familias a asisten 1 vez a la semana para recibir talleres sobre nutrición, salud, estimulación, exploración de los diferentes rincones de la educación inicial, basados en las actividades rectoras de la primera infancia como el juego, el arte, la exploración del medio y la literatura. Reeducamos las familias en fortalecer los lazos afectivos, enseñando valores, autoestima, asesoría psicológica, trabajo social y nutricionista que cuidad la salud de nuestros niños. El ICBF patrocina este programa como ente rector garante de los derechos de los niños en Colombia. Actualmente sigo desarrollando este labor tan importante para entregar individuos útiles a nuestra sociedad con el acompañamiento de las familias.
