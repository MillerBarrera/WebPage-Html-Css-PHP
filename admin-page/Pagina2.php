    <p class="textobanner">¿COMO EL JUEGO CREA VINCULOS EN LA FAMILIA?</p>
    <div class="container">
    <div clase="Articulo">
        <article>
            <p class="texto0">
            La literatura en la educación inicial juega un papel relevante, puesto que definirá su comunicación escrita, aparte de ser la herramienta esencial para cimentar la personalidad de las niñas y niños, Se quiere llevar a la práctica que las familias se comprometan a incluir en su entorno como la literatura , expresiones artísticas y la exploración del medio, estableciendo vínculos afectivos con pares y adultos significativos, diferentes a los de su familia, a relacionarse con el ambiente natural, social y cultural,que es fundamental en el estudio frente a los pilares de educación inicial, como estrategia didáctica para la enseñanza de la literatura en la primera infancia.
            </p>


            <p class="texto1">
                Es necesario tener claro que la propuesta pedagógica debe estar articulada con el PEI ya que dicho
                documento es de suma importancia para la gestión pedagógica, partimos de este concepto para la
                elaboración del proyecto de aula en lectura creativa, para ello partimos de la teoría planteada por la
                Profesora Rossenblatt quien desde el modelo transaccional de la lectura y la escritura nos permite
                identificar como se presenta la relación afectivo – cognitiva en la enseñanza de la creación literaria
                para el desarrollo conceptual.

            </p>
            <p class="texto3">
                Las madres gestantes deben estimular al bebe de manera lúdica, leyendo cuentos, con expresiones
                artísticas y expresiones orales para con ellos, como son las canciones de arrullo muchas de ellas son de
                diferentes culturas y siempre se les inculca seguir con esos arrullos que ellas escucharon en sus
                familias. Con los niños- niñas lectura de cuentos y canciones infantiles las madres pueden crear cuentos
                de sus mismas experiencias con ellos con diferentes temas, nutrición, hábitos de higiene, rutinas,
                valores. Por eso la literatura es una herramienta para todas las edades. Padres, madres y agentes
                educativos deben disfrutar la literatura, demostrar sus habilidades artísticas, la literatura lleva a
                soñar, imaginar e innovar.
                A través de la lectura se enriquece el vocabulario, lo cual es muy beneficioso para ellos, puesto que
                aprenden nuevas palabras, sinónimos, debido al tipo de lenguaje que usan en las narraciones. Por tanto,
                con las expresiones orales se puede explorar diferentes acciones, a aprender valores, a ser más
                autónomos, a desarrollar confianza en sí mismos y a cuidar a los demás, a sentirse seguros, partícipes,
                escuchados, reconocidos.


            </p>
        </article>
